 Protocol-Oriented Programming (POP) is a paradigm that focuses on the use of protocols to define the structure and behavior of a system. Introduced by Apple in *Swift 2.0*.
 
 ---
 
 ### Key Concepts
 
 * **Protocols**
    * Define a blueprint of methods, properties, and other requirements that suit a particular task or piece of functionality.
 * **Protocol Composing**
    * Types are built by composing protocols.
 * **Protocol Conformation**
    * Protocols can Conform from other protocols to build complex requirements.
 * **Protocol Extensions**
    * Allow you to provide default implementations for the methods and properties defined in the protocol.
 
 ---
 
 ### POP vs. OOP
 * Object-Oriented Programming (OOP) focuses on classes and inheritance. It emphasizes encapsulation, inheritance, and polymorphism.
 * Protocol-Oriented Programming (POP) emphasizes the use of protocols to define behavior and composition over inheritance. It aims to avoid the pitfalls of deep class hierarchies.
 
 ---
 
 ### Benefits of POP
 * **Flexibility and Reusability:** 
    * By using protocols, you can easily swap out implementations and extend functionality without altering existing code.
 * **Testability:**
    * Protocols make it easier to write testable code by allowing you to mock or stub implementations.
 * **Decoupling:**
    * Protocols help in decoupling code, making it more modular and easier to maintain.
 
